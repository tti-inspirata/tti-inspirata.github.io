		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			撒母耳记描述的历史跨越130年，跌宕起伏的王朝更替，辅以复杂多变的人性翻转，构成了书卷的叙事主线，犹如一部古代以色列王朝的连续剧。我们喜爱阅读这卷书中的人物与情节，但也恰恰因为叙事的丰富多彩，我们或许<span style="color: #FF8124; box-sizing: border-box;">误以为这就是撒母耳记的全部</span>。
