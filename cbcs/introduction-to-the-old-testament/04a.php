		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			罗伯特·范诺伊所著的<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：撒母耳记》</span>超越了叙事本身，以立体的视角，为我们揭示情节与人物背后的神学意义。这些视角包括以色列王国的历史、全部圣经正典，尤其是具体事件与新约和基督之间的关联。例如，作者这样解读神拣选大卫（撒16）：“膏立这位新王标示着以色列崭新的开始，神的工作要向新方向发展，而这个方向最终会带来弥赛亚的盼望，所盼望的焦点就是‘大卫的根，又是他的后裔’（启22:16）。”<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>正如作者在导言中所提倡的，我们需要“<span style="color: #3E3E3E; box-sizing: border-box;">在启示和救赎不断扩展增加的背景下</span><span style="color: #3E3E3E; box-sizing: border-box;">来阅读每一段圣经故事</span>”<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			范诺伊选择的解经重点，并非为每段经文提供详细的解读，而是<span style="color: #3E3E3E; box-sizing: border-box;">撒母耳记的神学主题</span>。他将这卷书的内容概括在三个主题之下：<span style="color: #FF8124; box-sizing: border-box;">申命记视角；拥有神圣主权的崇高之神；君王和圣约。</span>作者对这些主题的总结（详见51－61页）提供了一个范例，使我们学到怎样从叙事文体中解读神学，从中看到神的主权如何运行在人类历史中，以及人如何用信心和顺服来回应神并承担自己的责任。
