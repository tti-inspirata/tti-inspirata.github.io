			智慧不是抽象的概念，而是关乎我们的生活实际。没有人想要成为愚昧人，但什么才是真智慧呢？<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：箴言》</span>的作者乔治·施瓦布指出，“智慧主要是在雅威面前过公义生活的技巧”<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>，使人能够在生活中讨神喜悦。因此箴言宣告说：“敬畏耶和华是智慧的开端。”（箴9:10）
