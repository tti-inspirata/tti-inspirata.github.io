			智慧不是从观察自然界而来，也不是通过学习得来，那么真智慧究竟从何而来？答案是：“<span style="color: #FF8124; box-sizing: border-box;">耶和华赐人智慧，知识和聪明都由他口而出。</span>”（箴2:6）我们当像所罗门一样，向神恳切地寻求，以得着属灵的真智慧，而不仅仅是所谓的“知识”。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			箴言以深入浅出的诗句，各种修辞手法，以及离合诗、交错配列、首尾呼应等文学结构，将创造主无处不在的智慧表达出来。箴言对当今的基督徒有重要的现实意义，它提供了道德伦理方面的教导，让我们去思想神、认识神，效法“智慧人的典范”<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>主耶稣基督。箴言就是教导人得智慧的书卷，好将天上的智慧表现在地上的日常生活里。让我们一起来“<span style="color: #FF8124; box-sizing: border-box;">侧耳听智慧，专心求聪明</span>”（箴2:2），得着耶和华所赐的真智慧。
