		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			路得记在圣经正典中位于士师记和撒母耳记之间。全书短小精悍，仅有4章篇幅，却向我们呈现了一个发生在士师秉政时期的动人故事。故事从拿俄米“<span style="color: #FF8124; box-sizing: border-box;">空空地回来</span>”（得1:21）开始，一路发展与推进，直至这个难题得到圆满解决。
