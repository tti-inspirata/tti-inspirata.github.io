		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			在混乱的士师时代，何以能有如此安详有序的故事发生？拿俄米的空乏又是如何逆转为“满满”？贾森·德理斯巴赫博士在<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：路得记》</span>中指出，这是神的旨意与人的责任协作所产生的结果。神的计划是要大卫建立王国，而当时故事中的路得、波阿斯和拿俄米对此浑然不知，他们只是身体力行活出了付代价祝福他人的“慈爱”。每个人都为他人着想，每个人都愿意慷慨向人施恩，这些美好品质一步步化解了难题。这不禁叫人惊叹神的计划是如何巧妙地透过人的品格逐步成就的。
		</p>
