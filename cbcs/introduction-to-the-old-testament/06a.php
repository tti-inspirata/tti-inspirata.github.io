		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			约伯记是智慧文学的杰出典范。传统智慧认为，善恶有报，行善之人自会在现世得到赏报，而现实却常与此相悖，约伯记所直面的正是这种张力。约伯的故事说明，在今世，个人受苦的原因或罪恶的问题可能永远都是个谜。因此，奥古斯特·康克尔博士在<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：约伯记》</span>中指出，寻求智慧并不能解决苦难的问题，也无法为我们解释宇宙的秩序。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			那智慧于我们有何用呢？如箴言9:10所言，“<span style="color: #FF8124; box-sizing: border-box;">敬畏耶和华是智慧的开端</span>”，智慧的最大功用是帮助我们更深理解敬畏耶和华的真意，以便在面对人生风浪时依然能甘心服于神的主权之下，以感恩谦卑的心活在充满奥秘的宇宙秩序之中。<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>就像约伯，他最终没有找到苦难的答案，却在“<span style="color: #3E3E3E; box-sizing: border-box;">炉灰中懊悔</span>”，全然降服在“旨意不能拦阻”的耶和华脚前（伯42:2、6），亲身向我们展示了什么是真智慧。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			这或许就是约伯记作者的本意，<span style="color: #3E3E3E; box-sizing: border-box;">他不是要回答</span>“良善的神为何允许世上有罪恶？””，而是透过约伯的经历引人思考这个古老的难题，给历世历代在痛苦中挣扎的人<span style="color: #FF8124; box-sizing: border-box;">带去安慰</span>，引导一颗颗破碎而困惑的心找到出路。<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>
		</p>
