		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			申命记，顾名思义，乃重申命令之意，源出希腊文的七十士译本书名deuteronomion（第二律法）<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>。<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：申命记》</span>作者尤金·梅理尔认为，“第二律法”之称是对申命记17:18的误解所致。<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>申命记不是对西奈律法的重申，而是在其基础之上的延伸和推进。因为西奈律法面对的是出埃及后漂流旷野的以色列民，而申命记则是针对即将进入应许之地定居的年轻一代。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			从内容而言，申命记属于告别辞，是摩西临终前对以色列人的告别演讲，意在<span style="color: #FF8124; box-sizing: border-box;">规劝他们遵耶和华的道而行</span>。从形式而言，申命记是圣约文本，与当时的赫人条约文本十分相似，均包含了前言、历史序言、总体条款、具体条款、祝福和咒诅、见证人等内容。<span style="color: #FFCA00; box-sizing: border-box;">[3]</span>从神学主题而言，申命记涵盖了神、以色列和约三个重要主题。<span style="color: #FFCA00; box-sizing: border-box;">[4]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			梅理尔指出，申命记讲述的是，神与以色列民立约并赋予他们守约的责任，为要<span style="color: #000000; box-sizing: border-box;">赐给他们无比丰厚的祝福</span>。若认真研读本书，“必能使人<span style="color: #FF8124; box-sizing: border-box;">更完整地</span><span style="color: #FF8124; box-sizing: border-box;">理解</span>神借着耶稣基督所赐的救赎恩典”<span style="color: #FFCA00; box-sizing: border-box;">[5]</span>。让我们与梅理尔博士一同透过书中一条条律法条规，去探寻和认识律法背后那位满有恩典的神，好得着藏在其中的满满祝福。
		</p>
