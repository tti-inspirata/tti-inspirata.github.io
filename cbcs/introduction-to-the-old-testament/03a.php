			<span style="color: #000000; box-sizing: border-box;">约书亚记堪称一部伟大的民族史诗，讲述了以色列人在约书亚的带领下渡过约旦河，攻取迦南诸城，最终得那地为业的历史。这卷书吹响了胜利的号角，处处彰显神的大能和信实，读来鼓舞人心，但同时也抛出了诸多令人困扰的释经难题：神真的让以色列人执行“</span><span style="color: #FF8124; box-sizing: border-box;">种族灭绝</span><span style="color: #000000; box-sizing: border-box;">”的命令吗？详尽无遗的分地清单有什么神学意义？我们是否需要了解迦南地的文化背景才能更恰当地理解书中的故事？</span>
