			<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：约书亚记》</span><span style="color: #000000; box-sizing: border-box;">的作者约瑟夫·科尔森博士对古近东文化有深入研究，他认为这卷书构思严密、巧妙，运用了当时为人熟知的写作体裁，并通过精心设计的文学框架和叙事线索，强调了“神为中心”这一主题。对于书中的疑难问题，科尔森博士引领我们跨越时空的障碍，详尽说明了那个时代的文本特点、语言惯例、人文和自然环境，在此基础上来诠释经文，这样做实际上就是“为找出经文原本的意图而读经，我们要以其自身的文学和历史规范为基础，而不是总要将我们自己未经验证的前提强加其上”</span><span style="color: #FFCA00; box-sizing: border-box;">[1]</span><span style="color: #000000; box-sizing: border-box;">。</span>
