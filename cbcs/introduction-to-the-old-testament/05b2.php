		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			以斯帖记有一个区别于其他书卷的特征，就是这卷书始终没有提及耶和华神的名字，这让读者感到困惑，我们难免会猜测，书中的人物和情节太巧合、太戏剧化了吧？这卷书叙述的是真实的历史吗？
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			在<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：以斯帖记》</span>中，作者盖瑞·史密斯表达了如下观点：“以斯帖记的神学建基于一个基本信念：隐藏的神拥有引导世上万民生活的主权。”<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>这卷书的另一个主题与神的主权相对应：“如何在敌对神子民信仰与行为的异教世界里生活。”<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			这本注释书中<span style="color: #FF8124; box-sizing: border-box;">最宝贵的信息是</span><span style="color: #FF8124; box-sizing: border-box;">史密斯博士的点评</span>。他将两千多年前的场景延申到今天读者的生活中，<span style="color: #3E3E3E; box-sizing: border-box;">让故事背后的属灵原则变得触手可及</span>。例如，由于哈曼的阴谋，以色列民族面临灭顶之灾，史密斯这样回顾当时的处境：“<span style="color: #3E3E3E; box-sizing: border-box;">这段故事讲到某些信神的人面对坏消息、特别是面对死亡威胁之际是如何回应的</span><span style="color: #FF8124; box-sizing: border-box;">。</span>经文没有列举一系列非暴力反抗的抽象原则，也没有把为美好事业献身的目标理想化。故事只是在讲述一群人在艰难处境之下的行动。不是所有的不同意见都值得为之而死，也不是每场冲突都会造成生命威胁。然而，末底改和以斯帖的回应能启发并鼓励某些信徒，因他们可能也正受到恨他们之人的强烈敌对。”<span style="color: #FFCA00; box-sizing: border-box;">[3]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			客观的描述，脚踏实地的思考，还有对神子民的怜悯之心，都包含在这简短精炼的点评之中。这卷注释书字里行间<span style="color: #FF8124; box-sizing: border-box;">体现着</span><span style="color: #FF8124; box-sizing: border-box;">史密斯博士的牧者之心</span>，引领读者<span style="color: #3E3E3E; box-sizing: border-box;">深入思索经文背后的原则，并应用在自己的生活处境中。</span>
		</p>
