		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			传道书用了大量篇幅讨论“虚空”。传道者思考日光之下的生活，怀疑人生是否有意义，神是否公义。因此<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：传道书》</span>的作者川珀·朗曼三世认为，这卷书实质上是在探讨“信仰危机”<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			传道书的费解之处在于它呈现的立场和声音。它大量描述生命的虚空，似乎与圣经其他部分强调神的主权和创造有冲突。针对传道书带来的常见困惑，朗曼从<span style="color: #FF8124; box-sizing: border-box;">结构和主题</span>两方面提供了解读的框架。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			从结构而言，朗曼认为，传道书属于“反思自传……本书头尾框架中那个声音所说的话才是权威的教导”<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>。结构呈现的两种不同声音，对应两个不同主题，一个是传道者所哀叹的生命虚空，另一个来自评论传道者思想的那位智慧人，他认为若要摆脱生命的虚空，我们需要“<span style="color: #FF8124; box-sizing: border-box;">敬畏神，谨守他的诫命，等候将来的审判</span>”<span style="color: #FFCA00; box-sizing: border-box;">[3]</span>。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			朗曼以全部正典（尤其是罗马书8:20）为背景，解读传道书与其他书卷的关系。他总结说，传道者的言论是针对“堕落在一切受造之物上造成的影响”<span style="color: #FFCA00; box-sizing: border-box;">[4]</span>，因世间万物都伏在败坏与咒诅之下。若要从这般虚空中得释放，最终的答案是耶稣基督，“<span style="color: #FF8124; box-sizing: border-box;">因他复活，生命有了新的意义</span>”<span style="color: #FFCA00; box-sizing: border-box;">[5]</span>。
		</p>
