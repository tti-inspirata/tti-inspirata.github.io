		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			圣经中的十二卷小先知书因篇幅短小而得名。按照马所拉传统的希伯来文圣经，十二卷小先知书的排列次序为何西阿书、约珥书、阿摩司书、俄巴底亚书、约拿书、弥迦书、那鸿书、哈巴谷书、西番雅书、哈该书、撒迦利亚书、玛拉基书。<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			除了叙事体裁的约拿书、向以东宣告审判的那鸿书以及讲述内心挣扎的哈巴谷书，每一卷小先知书都在悔改的呼吁和审判的警告之后，描绘了将来的盼望，就是神必拯救属他的百姓。这让我们看到，先知书的重点不在于预告未来，而是<span style="color: #3E3E3E; box-sizing: border-box;">要<span style="color: #FF8124; box-sizing: border-box;">警告百姓当下就悔改回到神面前</span></span>。最让人心得安慰的是，虽然百姓顽梗悖逆，神却<span style="color: #3E3E3E; box-sizing: border-box;">始终信实守约</span>，赐给他们将来的盼望。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			以下是各卷小先知书的信息概要，详尽而精彩的阐释见理查德·帕特森和安德鲁·希尔博士的<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：小先知书》</span>。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">何西阿书</span>：耶罗波安二世执政年间，先知何西阿指责以色列百姓背离圣约，呼吁他们回转向神，免遭亚述灭国之灾，神又指示他娶妓女歌篾为妻，亲身示范神对以色列始终不渝的爱。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">约珥书</span>：先知约珥以蝗灾预示耶和华的日子来临时的审判，警告陷入属灵困境的犹大百姓及时悔改，免受更大的审判。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">阿摩司书</span>：南国先知阿摩司在耶罗波安二世的强盛时期谴责以色列人拜偶像、道德败坏，并宣告神的审判必要临到。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">俄巴底亚书</span>：先知俄巴底亚宣告欺压犹大和耶路撒冷的以东必承受神的审判。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">约拿书</span>：神借尼尼微城悔改一事教导先知约拿和以色列人，神的爱可以临到所有人。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">弥迦书</span>：面对亚述帝国的威胁，先知弥迦向犹大和以色列百姓传讲毁灭和盼望的信息，陈明神的公义必要掌权。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">那鸿书</span>：先知那鸿向受亚述压迫的犹大百姓宣告，尼尼微即将覆亡。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">哈巴谷书</span>：约西亚作王初期，面对社会不公和罪恶当道，神似乎无动于衷，于是先知哈巴谷向神抱怨心中困惑，最终在属灵挣扎中转而信靠神。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">西番雅书</span>：约西亚作王初期，先知西番雅抨击犹大社会的罪恶，呼吁神子民在“耶和华的日子”来临前悔改。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">哈该书</span>：先知哈该激励被掳归回的犹大百姓起来重建耶路撒冷圣殿。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">撒迦利亚书</span>：先知撒迦利亚以一系列关乎将来的异象鼓励被掳归回的犹大百姓重建属灵复兴。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">玛拉基书</span>：先知玛拉基向灰心丧志的被掳归回群体重申神的爱，引他们恢复敬畏神的生活。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
