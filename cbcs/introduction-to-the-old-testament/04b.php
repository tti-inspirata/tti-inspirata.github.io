			只看书卷名，你可能会以为列王纪就是一部以色列版的“春秋战国记”。然而，你若跟随<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：列王纪》</span>的作者威廉·巴内斯博士，细细品味这部圣经书卷，必会惊讶地发现，这远非你所想象的典型史书。
