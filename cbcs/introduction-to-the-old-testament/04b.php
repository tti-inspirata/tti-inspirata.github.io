		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			的确，这卷书记录了以色列王国时代（包括后来分裂的南北两国）历代君王的作为。比照士师时代“没有王”的混乱，君主制带来国泰民安的盼望。然而，列王纪见证的却是这盼望步步破碎的历程。巴内斯博士总结说，根据列王纪作者“残酷的评价”，列王有“诸多的失败、偶而的成功”，而他们的王朝迈向“<span style="color: #FF8124; box-sizing: border-box;">最终看似必然的覆灭</span>”。<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>在巴内斯博士的详尽解说中，即便是所罗门兴建圣殿的辉煌篇章里，不祥的阴影也已若隐若现。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			耐人寻味的是，这部涵盖了以色列四百多年王国历史的巨著，花费在先知身上的笔墨却不亚于对君王的记述。巴内斯博士梳理的书卷大纲清楚呈现了这一点，在经文注释里，他对先知轶事也详加点评，因为先知所言所行在在传达神的心意。走投无路的以色列民仍有盼望，归根结底在于这位始终“<span style="color: #FF8124; box-sizing: border-box;">没有放弃他百姓</span>”<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>的神。
