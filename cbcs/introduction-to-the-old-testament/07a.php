		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			诗篇是圣经中深受历代信徒喜爱的书卷。我们之所以钟爱诗篇，是因为人所能有的任何一种情感都能在诗篇里得到表达和纾解。诗篇的主要基调并非愉快的情绪，而是<span style="color: #FF8124; box-sizing: border-box;">迷茫、忧伤和困惑</span>，但却被称为“赞美之书”，其原因就在于，<span style="color: #3E3E3E; box-sizing: border-box;">不论众位诗人的灵魂经过怎样的低谷和绝境，赞美都是他们最终的表达</span>。因此，“<span style="color: #3E3E3E; box-sizing: border-box;">赞美之书</span>”的称呼不是基于诗篇的内容，而是由<span style="color: #3E3E3E; box-sizing: border-box;">诗篇的最终形态</span>决定的。<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			从最终形态而言，诗篇由五卷书组成，每一卷末都是一首颂歌，而诗篇1和2则是全书的引言。马克·富塔托博士在<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：诗篇》</span>中指出，诗篇是经过精心编排的统一整体，有其目标和思想。诗篇1的目标是“妥拉”，即教导，诗篇2的中心思想则是“耶和华作王”。这一认识对我们诠释各首诗篇至关重要。<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FFCA00; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #3E3E3E; box-sizing: border-box;">你如何面对悲痛、忧伤、恐惧、怀疑、希望、焦虑和困惑？</span>让我们随着富塔托博士的解析，更加真切地体会诗人在不同境遇下的心境，同时效法诗人，在斑驳陆离的人生境况中仍持守与神的关系，<span style="color: #FF8124; box-sizing: border-box;">从各样不安的情绪中转向神</span>，赞美这位荣耀的大君王。
		</p>
