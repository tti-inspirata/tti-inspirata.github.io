		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			古代以色列史家似乎从不讳言本族群的失败，因为失败见证了人的无助和无能，呼唤着神的光照和怜悯，士师记便是这样一卷书。<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：士师记》</span>的作者劳森·斯通博士依循士师记所提供的线索，探究这个蒙神拯救的信仰群体如何与神所赐的伟大异象渐行渐远。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			这趟逆向的旅程从“约书亚死后”开始，因为进入应许之地并非终点，而是<span style="color: #FF8124; box-sizing: border-box;">新的起点</span>。以色列民本当继承先辈的信心和忠心，完成未竟的“征服”使命，但斯通博士将士师记1:1－3:6与约书亚记结尾的平行经文进行参照比对，显明这使命“<span style="color: #3E3E3E; box-sizing: border-box;">受侵蚀的严重程度，以及妥协所开启的不可阻挡的进程</span>”<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			对于士师记中最脍炙人口的英雄故事（3:7－16:31），斯通博士仔细研究了该书卷采用的叙事笔法，令人信服地证明这一系列故事意在<span style="color: #FF8124; box-sizing: border-box;">勾勒士师制度逐渐解体的趋势</span>，揭示出信仰妥协所导致的品格缺失使得英雄再有“<span style="color: #3E3E3E; box-sizing: border-box;">灵力</span>”也无法摆脱“<span style="color: #3E3E3E; box-sizing: border-box;">被一己之任性所囚禁</span>”<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>的困境，更无法阻遏群体的败坏和堕落。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			斯通博士指出，书卷最后部分，即17－21章所记述的故事正反映出圣约群体堕落的可怕后果——<span style="color: #FF8124; box-sizing: border-box;">整个以色列几乎到了自我毁灭的边缘</span>。然而，正是依托于这黑暗的背景，君王的使命才显得轮廓清晰，那就是“拯救以色列脱离最致命的敌人——它自己”<span style="color: #FFCA00; box-sizing: border-box;">[3]</span>。
