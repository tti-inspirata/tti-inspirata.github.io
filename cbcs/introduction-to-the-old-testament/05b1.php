		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			我们有一些耳熟能详的信仰“概念”，例如，神掌管万有、要相信神。但是，<span style="color: #3E3E3E; box-sizing: border-box;">这些“概念”如何体现在我们的日常生活中？</span>以斯拉记-尼希米记为我们提供了全方位的见证。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			这两卷书记载了以色列民回归并重建耶路撒冷的重大事件，时间跨度约120年。《房角石注释：以斯拉记-尼希米记》的作者盖瑞·史密斯认为，书中最突出的主题，是“<span style="color: #FF8124; box-sizing: border-box;">神对以色列历史各个层面的全能掌管</span>”<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>。具体而言，他总结了以下几个方面：神激励外邦人允许以色列民回归故土，并提供行政和财力支援；神掌管他子民的行动与决定，激励他们在困境中重建耶路撒冷；神的主权体现于他降祸与赐福的权柄。<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			这两卷书同时也记载了神的子民如何回应神，正如史密斯所说，“<span style="color: #FF8124; box-sizing: border-box;">靠着这种信神掌管一切的基本信仰</span>，以斯拉与同他一起的会众就有了每日的勇气与实际的信心” <span style="color: #FFCA00; box-sizing: border-box;">[3]</span>。他们的信心，体现在一系列实际行动中：回归，建造圣殿，敬拜，认罪，悔改，祷告，与不圣洁的群体分离，学习律法并立约信守。这些信仰常规，我们或许习以为常，但是在以斯拉记-尼希米记中，它们有了清晰的方向，构成了有信心的回应。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			一个受到神惩罚的群体，却在神的旨意下，按着神的时间和引导，重新<span style="color: #FF8124; box-sizing: border-box;">恢复圣洁的生活</span>。他们所经历的神的大能，以及他们对神大能的回应，为今天的读者展示了一个重要的神学主题：神的主权与人的回应如何一起做工，成就神的旨意，并祝福神的子民。<br style="box-sizing: border-box;"/>
