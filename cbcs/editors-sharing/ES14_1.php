			莫朗博士针对这样一个常见的疑问，提出了他独到的见解。他说，这段经文出现了“<span style="color: #FF8124; box-sizing: border-box;">陈述-命令</span>”之悖论，即，陈述性的经文讲到神为信徒成就的事，命令性的经文是对信徒的要求。在保罗看来，所陈述的真理，并不免除命令的必要性。神的主权和人的责任看似互相矛盾，但实际上，两者都是我们应当认真对待的真理，这构成了人眼中的悖论。作者在这卷注释书中多次谈及保罗的“悖论神学”，认为“我们低估了保罗接受悖论的能力……悖论就处于生命的核心，接受这种悖论式的思考方式，就是学习在神的奥秘中生活。此外，接受悖论也有助于我们尽可能恰当地理解生命中的许多复杂难题”<span style="color: #FFCA00; box-sizing: border-box;">[4]</span>。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			“<span style="color: #FF8124; box-sizing: border-box;">旧事已过，都变成新的了！</span>”（林后5:17），这是神已做成的工，也是对我们每日的呼召。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			既然单单靠恩典得救，何不继续犯罪呢？莫朗博士告诉我们：“任何人若如此曲解神的恩典，就根本没有真正领会何为神的恩典。”<span style="color: #FFCA00; box-sizing: border-box;">[5]&nbsp;</span>神白白的恩典至深至广，但绝不容人亵慢、践踏，我们应当全心拥抱新的身份，领受新的呼召，活出新的生命。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
