			纵观这些历史背景，我们看到以色列人那时崇拜的偶像以及它们代表的具体含义，因年代久远，难有明确答案。但是那些偶像有一共同点，它们都源自异教。这说明以色列的偶像崇拜，其实质是<span style="color: #FF8124; box-sizing: border-box;">信仰的混合主义</span>。以色列民虽然有神的律法，晓得耶和华的名，却没有把他当作唯一的真神来敬拜，反而转向异教的神明。汤普森评论说，以色列人似乎希望“通过充满巫术色彩的敬拜仪式获得雅威对家庭、田地、国家的祝福”，然而这样的方式违背了“西奈之约里通过信靠和顺服经历圣约祝福”的约定。<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>
