			第一是谦卑（131:1）。眼目高傲、内心狂傲的人，往往内心缺乏安全感，所以需要快跑抓住很多东西，以显明自己举足轻重。想要掌控超出人掌控范围的事，自然会生出更多的焦虑、惧怕和不安。只有谦卑俯伏在神脚前，相信神的旨意，仰望神的慈爱，倚靠神的大能，才能得享内心的平静。
