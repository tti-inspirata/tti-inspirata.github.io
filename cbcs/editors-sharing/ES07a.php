			在忙碌奔波的现代社会，最稀缺的不是时间，而是内心的平静与安息。曾经深深被一幅插图打动，是一个小男孩下巴靠着大人肩头熟睡的画面。那副酣睡如泥的样子，让人心安，也叫人羡慕。我一下子就想起大卫在诗篇中的描绘：“我的心……好像断过奶的孩子在他母亲的怀中。”（诗131:3）曾多次默想过这段经文，也很渴望自己的心在神面前也能像诗人一样，像断奶的孩子在母亲怀中，不哭不闹，安心满足。但是要如何才能真正得着诗人所描画的这般“平稳安静”呢（诗131:2）？马克·富塔托在<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：诗篇》</span>中对诗篇131的释义，<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>带我找到了自己还未挖到的宝藏。<br style="box-sizing: border-box;"/>
