		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			保罗为何时隔十年在狱中向腓立比信徒写了这样一封书信？<span style="color: #FF8124; box-sizing: border-box;">他要向这群圣徒表达什么样的信息？</span>首先，保罗是要对腓立比信徒的馈送表示感谢（腓1:5，4:18），但从根本上来说，保罗是为了和腓立比信徒分享自己追求认识和效法基督的渴望与见证，从而鼓励他们也能像他那样义无反顾地效法基督。<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			保罗深深惦念这群腓立比信徒，甘愿舍弃那好得无比的与基督同住，而暂时活在肉身，好叫他们生命得益处（腓1:23－25）。保罗这样“体会基督耶稣的心肠”（腓1:8），不仅给腓立比信徒带去了安慰和激励（腓1:8），也触动着今日定意要跟随耶稣脚踪的你和我。让我们随着菲利普·康福德博士的<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：腓立比书》</span>，去领略保罗效法基督的美好见证，在自身的处境中跟随基督，使我们身边的人同得勉励，共同兴旺福音。
