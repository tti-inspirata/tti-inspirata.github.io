		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：使徒行传》</span>的作者威廉·拉金指出，宣教的神（Missio Dei）在使徒行传中至关重要。他围绕着“三位一体的神借着众使徒宣教”这一神学主题，具体分析三一神的每一位格如何亲自参与并透过使徒和教会来施行救恩。拉金认为，使徒行传尤为关注“对福音恰当的回应、救恩的祝福、救恩的普世性”，而这些都服务于本书意在传扬福音的主旨。<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>
		</p>
