		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			使徒行传是路加福音的续篇，两者都是致献给提阿非罗大人的书卷（路1:1；徒1:1）。路加在路加福音里阐明了何为福音，即“<span style="color: #FF8124; box-sizing: border-box;">已完成的救恩</span>”，在使徒行传中则证实了福音要传给“万邦”（路24:47），即“<span style="color: #FF8124; box-sizing: border-box;">施行的救恩</span>”。<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>因此，使徒行传讲述的是神借着圣灵差派他的使徒宣扬福音的故事。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
