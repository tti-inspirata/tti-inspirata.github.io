		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			启示录的研究进路大概有两类，一类视之为约翰所见异象的真实呈现，另一类则假设作者不过是采用了天启文学的体裁，来向目标受众传达信息。<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：启示录》</span>的作者小罗伯特·马尔霍兰则另辟蹊径，他指出，叙事鉴别学的视角为我们开启了一扇走进约翰异象世界的门，使我们的研究超越文本的形式或体裁，发现根植于宏大叙事背景的释经线索。<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			约翰的叙事世界就是“<span style="color: #FF8124; box-sizing: border-box;">犹太故事</span>”，围绕犹太人的弥赛亚盼望划定，又大大颠覆了他们旧有的盼望。在他笔下，耶稣就是以色列人所盼望的弥赛亚，他是诚实的殉道者，使以色列人从罪中得释放，又从死里首先复生，统治神的祭司国度，将来还要驾云降临，成就完全的复兴和最终的胜利。<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			作者认为，启示录至少有三个主题：<span style="color: #FF8124; box-sizing: border-box;">耶稣基督的启示；十字架的功用；新耶路撒冷和堕落的巴比伦。</span>约翰的异象完全是关乎耶稣基督，这异象启示了十字架的真意，又是“向耶稣的跟随者吹响的号角，呼召他们在堕落的巴比伦，即他们身处的世界中，活出忠心的新耶路撒冷国民的样式”。<span style="color: #FFCA00; box-sizing: border-box;">[3]</span>
		</p>
