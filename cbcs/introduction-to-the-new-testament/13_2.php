			<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：约翰一二三书》</span>的作者是菲利普·康福德博士和温德尔·霍利博士，他们认为约翰的福音书和三卷书信关系密切。约翰一书是约翰福音的实践神学释义，旨在帮助读者深入理解耶稣的教导，并应用在个人和教会生活中，<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>书中论到多个与实际生活息息相关的神学主题：与神相交、认罪、爱神、爱弟兄姐妹、洁净自己、辨明真理等。约翰二书可以说是“约翰一书的缩微版”<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>，再现了一书的重要主题：遵行真理、彼此相爱、警惕假教师、忠于使徒教导。“约翰三书着重应对一书提到的问题——属灵的精英领袖反抗使徒的权柄”<span style="color: #FFCA00; box-sizing: border-box;">[3]</span>，对比了忠于使徒教训的领袖和造成分裂的领袖，勉励读者按真理而行。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			这三卷约翰书信“<span style="color: #3E3E3E; box-sizing: border-box;">蕴含着各个时代基督教观念中<span style="color: #FF8124; box-sizing: border-box;">最基本的神学、伦理与实践性真理</span></span>”<span style="color: #FFCA00; box-sizing: border-box;">[4]</span>，我们可以从中看到1世纪末的基督徒如何活出耶稣的教导，并获得初代教会的宝贵教牧经验，从而在今天的时代学习遵行真理并活在爱中，服侍基督，建造教会。
