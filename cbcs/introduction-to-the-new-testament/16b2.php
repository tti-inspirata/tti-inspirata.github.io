			“基督再来”是帖撒罗尼迦前后书的重要神学主题，书中呈现的基督论强调升天后的基督很快就会再来，拯救信徒并毁灭不法之人，那时信徒要经历全然的成圣。<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>那今天仍走在成圣道路上的我们该如何预备自己迎接主耶稣？菲利普·康福德博士在《<span style="color: #FF8124; box-sizing: border-box;">房角石注释：帖撒罗尼迦前后书》</span>中指出，圣灵会引导基督徒从救赎走向完全成圣，或苦或乐，他都在我们当中，使我们满怀信心和盼望地等候主耶稣的再来。<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>
		</p>
