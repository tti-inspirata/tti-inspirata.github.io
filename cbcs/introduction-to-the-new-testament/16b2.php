			帖撒罗尼迦前后书是新约中成书最早的著作之一，也是所有保罗书信中最简单易懂的书卷，又被称为<span style="color: #FF8124; box-sizing: border-box;">初代教会最基本的福音宣告</span>。这两封书信是由保罗和西拉于公元51年写给帖撒罗尼迦人的，不只是针对他们当中出现的不当做法给出教导，也回应了他们提出的有关基督再来的问题。
