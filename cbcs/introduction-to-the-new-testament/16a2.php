			“<span style="color: #FF8124; box-sizing: border-box;">在所有保罗书信中，没有一封比歌罗西书更高举基督</span>”<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>。保罗笔下的“基督颂”荣耀恢弘，高举基督为万有之首、全宇宙之主和教会全体之首（西1:15－20）。然而，保罗的“超越基督论”并非在阐述抽象的教义，而是要指示教会如何实际住在基督里，活出以基督为中心的新生命。
