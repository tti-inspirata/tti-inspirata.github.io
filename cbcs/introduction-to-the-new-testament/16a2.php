			<span style="color: #FF8124; box-sizing: border-box;">《房角石注释：歌罗西书》</span>的作者彼得·戴维兹认为歌罗西书包含一个主要论题和一个次要论题。主要论题是“基督的全然丰足”。基督对受造物和教会拥有至高的主权，他赐下全备的救恩，也是属灵生活的中心。次要主题则关乎伦理，即我们的生活方式要被基督管理，成为新造的人。<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>然而，无论在1世纪还是今天，总有各种力量要主导我们的生活，使我们转离基督。歌罗西人面临的诱惑可能来自犹太规条、异教观念、文化习俗等，保罗指示他们要认识基督超越的权能，明白自己与基督联合的关系，活出在基督里的丰盛生命，并盼望基督荣耀的国度。我们对基督的认识越深，就越能经历生命的更新，因此，这卷书可以说是一部“<span style="color: #FF8124; box-sizing: border-box;">活出基督徒健康生命的实际指南</span>”<span style="color: #FFCA00; box-sizing: border-box;">[3]</span>，为“评估今日的属灵教导提供了有益的指导原则”<span style="color: #FFCA00; box-sizing: border-box;">[4]</span>。
		</p>
