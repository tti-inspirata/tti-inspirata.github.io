			腓利门书是保罗书信中最简短的一封。腓利门是歌罗西教会的一位信徒（西4:9），家境殷实，曾与保罗同工。他手下的奴隶阿尼西母不知何故出逃，在逃离期间信主。于是，<span style="color: #FF8124; box-sizing: border-box;">保罗写了这样一封类似调解书的标准希腊书信</span>，<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>请求腓利门能不计前嫌，重新接纳主里新生的弟兄阿尼西母。
