			身为带领腓利门信主的人，保罗并未发号施令要求腓利门接纳阿尼西母，而是语重心长地凭着爱心恳请腓利门能以新的眼光看待阿尼西母，按照主内家人的关系对待他。保罗的请求无疑和周遭世界格格不入，甚至可能被视为破坏社会秩序，保罗也因此措辞审慎。保罗因着基督的缘故力劝腓利门饶恕和接纳曾背弃他的奴隶阿尼西母，<span style="color: #FF8124; box-sizing: border-box;">这在今天依然值得我们去深思和效法</span>。
