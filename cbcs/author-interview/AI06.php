			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			我认为，这两条轨线告诉了我们阅读圣经叙事的方法。正如格雷达努斯（S. Greidanus）所说的（《救赎历史与讲道》，Pro Rege19/2[1990]14），“<span style="color: #FF8124; box-sizing: border-box;">圣经所呈现出来的，不是一系列有趣之人的传记</span>，而是一个历史——关于神国度来临的历史。”这意味着我们如果要在旧约叙事中寻找今天生活的意义，那么最重要的是把故事放在救赎历史的语境中来读。我建议，读者可以在读每个故事单元时问以下三个问题，帮助他们辨明这个故事与今天神子民的联系所在：
		</p>
