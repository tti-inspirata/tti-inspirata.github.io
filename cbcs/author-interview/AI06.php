		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			虽说如此，撒母耳记上下还是讨论了一些在堕落世界中的议题，是不同时代地区、古代世界或现代世界都共有的。它们是很平常之事，比如婚姻、家庭、对统治者的不满、友情、性失徳、权力争斗、正确和不正确的宗教崇拜，这些都在叙事中有所细节描述，并与故事中人们的生活相关联。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			我举个例子。在撒母耳记上13、14和15中，扫罗反复尝试用敬虔的语言和宗教行为来掩饰他对神诫命的不顺服和自私轻率的行为。事实上，这一策略最后变成了一种模式，最终导致神差遣撒母耳来告诉扫罗：主已经厌绝他作王（撒上15:22,23）。我们在这里可以学到，宗教语言和行为和“行走在神的道路”并不一定相合。决定一个人行为品质的，并不是他内外的宗教言语和行为。更重要的是<span style="color: #FF8124; box-sizing: border-box;">一个人的行为是否出于对神的爱和对他话语的信赖，以及它是否符合神的命令</span>。这在撒母耳、扫罗的时代和在今天都同样真实。施行宗教或礼仪的行为却没有一颗渴望顺服神命令的心，在主看来不仅是不可接受的，更是可憎恶的。信仰的群体一直有一个危险，就是趋向于空有宗教仪式的假冒为善，今天的时代并不比撒母耳、扫罗的时代更好。
		</p>
