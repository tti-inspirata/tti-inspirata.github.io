				<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
					耶稣问：“彼得，你以神的爱（agapaō&nbsp;[TG25,&nbsp;ZG26]）爱我吗？”
				</p>
				<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
					彼得答道：“主啊，我很喜欢你（phileō[TG5368,&nbsp;ZG5797]）。”
				</p>
				<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
					有点泄气的耶稣再次问道：“彼得，你以神的爱（agapaō&nbsp;[TG25,&nbsp;ZG26]）爱我吗？”
				</p>
				<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
					彼得回答：“主啊，我非常喜欢（phileō[TG5368,&nbsp;ZG5797]）你。”
				</p>
				<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
					耶稣放弃了：“彼得，你喜欢（phileō[TG5368,&nbsp;ZG5797]）我吗？”
				</p>
				<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
					彼得总结道：“我不是说了吗？我当然喜欢（phileō[TG5368,&nbsp;ZG5797]）你。”
				</p>
			</section>
