		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; font-size: 16px; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="font-size: 16px; box-sizing: border-box;">许多学者与牧者（如Hunter、McKay 1985）认为这个片段讲到两种层次的爱，因为这段经文使用了两个希腊文动词。如果真是如此，那么这段经文的意思就会是这样：</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="font-size: 16px; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
