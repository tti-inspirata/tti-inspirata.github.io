		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; font-size: 16px; box-sizing: border-box;">当然，上述译法有点自由，但差不多能反映出这种解读必然包含的意义。若如此解读，耶稣就是在向彼得那个较低层次的爱屈服。这是不可能的。甚至可以说，它与这段经文的真正意义和遣词造句都格格不入。</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; font-size: 16px; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; font-size: 16px; box-sizing: border-box;">第四卷福音同时用这两个词（<em style="box-sizing: border-box;"><span style="letter-spacing: 0.4px; color: #FF8124; box-sizing: border-box;">agapaō</span></em>[<sup style="font-size: 12px; box-sizing: border-box;">TG</sup>25,<sup style="font-size: 12px; box-sizing: border-box;">ZG</sup>26]与<em style="box-sizing: border-box;"><span style="letter-spacing: 0.4px; color: #FF8124; box-sizing: border-box;">phileō</span></em>[<sup style="font-size: 12px; box-sizing: border-box;">TG</sup>5368,&nbsp;<sup style="font-size: 12px; box-sizing: border-box;">ZG</sup>5797]）来指父对子的爱、父对门徒的爱、耶稣对拉撒路与他所爱之门徒的爱以及耶稣对“所爱”之基督徒的爱（Brown 1966:498）。现今大家一致认为这两个词是同义词（Brown, Carson, Beasley-Murray, Burge, Blomberg, Keener, Köstenberger）。</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; font-size: 16px; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; font-size: 16px; box-sizing: border-box;">再者，这三节简短的经文中有四个词对——两个词表示“爱”、两个词表示“知道”、两个词表示“养”（参“喂养”与“牧养”，NLT），两个词表示“羊”（参“小羊”，NLT）。约翰借由词汇的多样性来强调此处信息的全面性：人若深爱耶稣，就会深切希望牧养耶稣的羊群。耶稣三次讲到这层意思，强调此事至关重要。</span>
		</p>
