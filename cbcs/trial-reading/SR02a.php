		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			敬拜是当今教会讨论的重要问题，同其他涉及神学和实践的问题一样，教会对敬拜的理解也有分歧。人们提出的问题包括：如何敬拜，谁参与敬拜，在何时何地敬拜，甚至为何要敬拜。人们讨论的问题还有：敬拜是否必须由群体共同参与，或者敬拜同样可以（甚至最好）是个人行为，关乎个人与神独处时的状况。更为根本的问题是，<span style="color: #000000; box-sizing: border-box;">人们实际上对什么是敬拜和什么不是敬拜也有不同看法</span>。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			就本书宗旨来看，我们将敬拜理解为神的子民对神的服侍，因为这种看法最准确地表达了英语译文背后的希伯来语、希腊语原文含义。将敬拜理解为服侍的看法显然比我们通常对敬拜的定义更广泛，但这种看法很重要，因为它强调了<span style="color: #FF8124; box-sizing: border-box;">我们蒙召去做什么</span>（即服侍和做工），而不只是我们应当对神有什么感觉。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			这个背景会对我们理解旧约书卷利未记大有裨益。大部分基督徒没有花费原本应该花费的时间来学习利未记，因为这部书显得那么遥远，似乎与我们日常生活的任何内容都格格不入。<span style="color: #FF8124; box-sizing: border-box;">将利未记视为敬拜指南<span style="color: #3E3E3E; box-sizing: border-box;">可以使该书更容易理解</span></span>。对古代以色列人（包括指导普通民众的祭司）来说，<span style="color: #3E3E3E; box-sizing: border-box;">利未记就是敬拜指南</span>，它回答了笔者刚才提及的许多问题，尤其包括：谁可以参与敬拜，何时敬拜，以何种方式敬拜，百姓如何使自己配得上神的圣洁（或至少在神眼中可以被接受）以便可以参与敬拜。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			利未记不是一卷死书，只关乎死的祭牲，利未记是一卷活书，教导以色列人如何敬拜活神，以及作为活生生的神子民如何行事为人。虽然我们在今日的敬拜程序中不再采取相同的“<span style="color: #000000; box-sizing: border-box;">做法</span>”，但神依然<span style="box-sizing: border-box;">期待<span style="color: #FF8124; box-sizing: border-box;">我们的敬拜保持圣洁</span></span>，并符合他的要求。我们由此可以认识到，利未记的大部分内容仍可应用（或适用）于我们自己的情况。
		</p>
