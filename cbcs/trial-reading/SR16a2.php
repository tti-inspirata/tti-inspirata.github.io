			<span style="font-size: 16px; box-sizing: border-box;">歌罗西书此处的经文将受造界看作是<span style="color: #3E3E3E; box-sizing: border-box;">偏离或敌对</span>神以及神旨意的。以赛亚书11:6－9和65:17－25描绘了受造界被更新后的和谐与平安。但是，这些宽泛、诗化的描述如何体现在现实中呢？这是我们难以设想的，也许已经超出了人类的理解能力。<span style="letter-spacing: 0.4px; box-sizing: border-box;">我们缺乏理解力，还体现在对下面这处经文的的探讨中：</span><span style="color: #FF8124; letter-spacing: 0.4px; box-sizing: border-box;">“天上的”与神和好，这究竟是什么意思呢？</span></span>
