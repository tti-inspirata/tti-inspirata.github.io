			<span style="font-size: 16px; box-sizing: border-box;">在前面的经文中，保罗精彩讲述了基督的超越和荣耀，现在却将“十字架”——<span style="color: #FF8124; box-sizing: border-box;">他的十字架</span>——并列摆放在此。要知道，在罗马社会中，人们极其鄙视十字架一词，甚至认为谈话时提到这个字眼都是不礼貌的。（事实上，直到君士坦丁时期，基督徒才开始使用十字架的标志。）没有什么比被钉十字架更羞辱的了，这种死刑是专门给那些不配做人的人预备的。<span style="color: #000000; box-sizing: border-box;">华丽的诗章就这样直冲云霄，又陡然以吊诡之言结束。</span></span>
		</p>
