			<span style="font-size: 16px; box-sizing: border-box;">首先，<span style="color: #FF8124; box-sizing: border-box;">神喜欢叫一切的丰盛在基督里面居住</span>。保罗借用了犹太人关于神（或智慧，或神的灵）居住在人里面的观念，并且进一步深化。神的确在耶稣里面居住，但在他里面居住的乃是“神一切的丰盛”。<span style="letter-spacing: 0.4px; box-sizing: border-box;">这就是说，没有人能比耶稣更多地得着神。“<span style="letter-spacing: 0.4px; color: #3E3E3E; box-sizing: border-box;">神乐意</span>”也是一个旧约用语，表示神拣选某人或某事。对神来说，道成肉身不是偶然之举，也不是无奈之举，而是一个深思熟虑的选择。在耶稣里，我们可以看到神一切的丰盛，就像约翰所说的：“神赐圣灵给他是没有限量的。”（约3:34）此处经文再现了约翰笔下那句话的用意——即耶稣所说的：“人看见了我，就是看见了父。”（约14:9）这也再次表明，我们千万不能硬生生把耶稣和父神分隔开，仿佛其中一位很好而另一位逊色许多。耶稣和父神完全是一体的。</span></span>
