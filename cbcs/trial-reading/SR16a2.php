			<span style="font-size: 16px; box-sizing: border-box;">神给我们带来和平，不是凭借战斗获胜所洒的鲜血，也不是凭借勇士杀敌所需的力量，而是借着非人的酷刑所流的血，且这极刑是行在他儿子身上的。这一吊诡性的真理是保罗给基督徒生命树立的典范，因为尽管我们的主在复活的彼岸得荣耀、被高举，永远活着，但我们仍在复活的此岸。<span style="color: #000000; box-sizing: border-box;">我们的胜利也像他的胜利一样，不是通过威力来赢取的，</span><span style="color: #FF8124; box-sizing: border-box;">而是借着牺牲和软弱</span><span style="color: #000000; box-sizing: border-box;">。</span><br style="box-sizing: border-box;"/></span>
