			<span style="font-size: 16px; box-sizing: border-box;"><span style="letter-spacing: 0.4px; box-sizing: border-box;">这无疑是约翰福音中</span><span style="letter-spacing: 0.4px; color: #000000; box-sizing: border-box;">较受争议的经文之一</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">。对这段经文的诠释主要有两种，最主要的区别就在于是否要将句子断在希腊原文的7:37末尾，从而将该节与7:38一开始的“</span><span style="letter-spacing: 0.4px; color: #000000; box-sizing: border-box;">信我的人</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">”这一词组分开。如果按照分隔开来的断句方法，则这段经文应作“人若渴了，可以到我这里来喝。就如经上所说：‘信我的人，从他心中要流出活水的江河来’”。</span><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
