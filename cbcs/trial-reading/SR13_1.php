					东方教父（如俄利根；另见Westcott、Lindars、Barrett、Fee 1978、Kysar、Morris、Carson、Ridderbos、Köstenberger、KJV、RSV、NASB、NIV、TNIV）认为是“<span style="color: #FF8124; box-sizing: border-box;">信徒</span>”；而西方教父（如特土良；另见Dodd、Bultmann、Kilpatrick 1960、Brown、Schnackenburg、Bruce、BeasleyMurray、Comfort、Michaels、Meyken 1996、Whitacre、Marcus1998、Burge、Blomberg、NRSV、NLT）则主张是<span style="color: #FF8124; box-sizing: border-box;">基督</span>。Meyken（1996:163－166）认为，虽然“<span style="color: #3E3E3E; box-sizing: border-box;">信我的人</span>”属于7:38，但耶稣仍是所指的对象。
				</p>
