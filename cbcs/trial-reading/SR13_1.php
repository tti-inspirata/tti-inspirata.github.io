			<span style="font-size: 16px; box-sizing: border-box;">事实上，这个问题很难有最终答案。如果把信徒放在7:37中，就会形成有趣的交错配列结构，即两个主语（渴了的人与信徒）一前一后，将动词（来与喝）框在当中。然而，假如“<span style="color: #FF8124; box-sizing: border-box;">信徒</span>”一词作为7:38的开头，那么“如经上说”这一套语就会出现在引文中间来修饰小品词，这是约翰福音，实际上也是整个新约独有的模式。此外，渴了的人要先喝，然后再把水分给其他人——这种比喻虽有可能成立，却不符合此处的语境（<em style="box-sizing: border-box;"><span style="color: #FF8124; box-sizing: border-box;">Schnackenburg</span></em>）。</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="font-size: 16px; box-sizing: border-box;">不过，如果耶稣指的是自己，为何要说“<span style="color: #FF8124; box-sizing: border-box;">从他腹中</span>”（<em style="box-sizing: border-box;"><span style="color: #FF8124; box-sizing: border-box;">Fee</span></em>, <em style="box-sizing: border-box;"><span style="color: #FF8124; box-sizing: border-box;">Carson</span></em>）？<span style="letter-spacing: 0.4px; color: #000000; box-sizing: border-box;">我认为答案就</span><span style="color: #000000; letter-spacing: 0.4px; box-sizing: border-box;">在上下文中。耶稣</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">认为自己最终成全了倒水仪式，而5－10章的整个上下文都是在讲基督论。如果耶稣说圣灵所出之处是信徒而非他自己，就会显得很突兀，从14:26; 16:17来看更是如此，这两节说基督差遣圣灵执行他的使命。使用“他”一词符合耶稣自称“人子”的习惯。因此，</span><span style="color: #000000; box-sizing: border-box;"><span style="letter-spacing: 0.4px; box-sizing: border-box;">基督论的诠释最符合上下文</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">。</span></span></span>
