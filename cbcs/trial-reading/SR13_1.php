			<span style="font-size: 16px; box-sizing: border-box;">如果将句子从7:38当中断开，则读作“信我的人若渴了，可以到我这里来喝。就如经上所说：‘<span style="color: #000000; box-sizing: border-box;">从他</span><span style="color: #3E3E3E; box-sizing: border-box;"><span style="color: #FF8124; box-sizing: border-box;">[基督]</span><span style="color: #000000; box-sizing: border-box;">心中要流出活水的江河来</span>’</span>”。“信我的人”可属于任何一个从句，因此真正的问题是，圣灵究竟是从信徒还是从基督里流出来的（“活水的江河”；参4:13－14; 7:39）。</span>
