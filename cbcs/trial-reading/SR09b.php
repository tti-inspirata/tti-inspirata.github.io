			<span style="font-size: 16px; box-sizing: border-box;">但以理的这番祷告与摩西的祷告<span style="color: #3E3E3E; box-sizing: border-box;">极为相似</span>（出32:12－14），这并不叫人意外，因但以理不但孜孜不倦地研读耶利米书（9:2），也长期学习摩西书卷。但以理求主为他自己的子民、自己的城采取行动的时候，他的话听起来<span style="color: #FF8124; box-sizing: border-box;">仿佛出自摩西之口</span>（出5:22－23; 6:1; 32:11－13）。按着但以理对圣经的了解，神采取行动的时间到了；但是，他知道这一切仍取决于大有怜悯的神。最后，这位神仆人的祷告得蒙垂听（9:17, 23），且成为神主权行动构架中不可分割的一部分。</span>
