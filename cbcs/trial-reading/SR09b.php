					<span style="color: #3E3E3E; box-sizing: border-box;">第一</span>，主曾救他的百姓脱离埃及，所以他现在也可以为他们施行拯救（9:15, 18）。<span style="letter-spacing: 0.4px; box-sizing: border-box;">第二</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">，主名的荣耀与他百姓的命运相关联。主当为自己名的荣耀拯救他们（9:15, 17, 19）。这五节经文的支点为9:17。它提到了但以理的</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">第三</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">个见解——神的圣所荒凉了；所以，神及神的名被人嘲笑（9:16, 18）。</span><span style="letter-spacing: 0.4px; color: #FF8124; box-sizing: border-box;">第四</span><span style="letter-spacing: 0.4px; color: #FF8124; box-sizing: border-box;">点是关键</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">，因但以理说他恳求神为百姓出手，是因为神有大怜悯（9:16, 18）。</span>
