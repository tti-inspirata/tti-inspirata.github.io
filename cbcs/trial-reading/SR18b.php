		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="box-sizing: border-box;"><strong style="box-sizing: border-box;"><span style="color: #000000; box-sizing: border-box;">启示录 7:1－17 释义</span></strong></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;">第七章的异象关乎如何把属神者与不属神者分别开来。</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="box-sizing: border-box;"><span style="color: #000000; box-sizing: border-box;">在犹太历史上，<span style="color: #FF8124; box-sizing: border-box;">标注记号</span>以指定和分别的观念起源于逾越节的记载。当时是把记号标注在<span style="color: #FF8124; box-sizing: border-box;">神子民的门上</span>，使他们在审判临到埃及时能够逃脱（出11－12）。</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">以西结的喻象对此稍作变化，记号标注在</span><span style="letter-spacing: 0.4px; color: #FF8124; box-sizing: border-box;">义人的额上</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">，神对以色列中拜偶像者进行审判时，义人便能逃脱（结9）。</span></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;">这印不仅仅是一个记号，还与<span style="color: #FF8124; box-sizing: border-box;">受印者的本质</span>有关。随着异象渐次展开，约翰看见堕落巴比伦的国民也在额上受了印，这印与那“名”，即兽的本质有关（13:16－17）。约翰也看见，神的新耶路撒冷的国民在额上有神和羔羊的名（14:1; 22:3－4）。</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;">根据旧约，<span style="color: #FF8124; box-sizing: border-box;">前额</span>代表某人内心与神的关系如何。神子民的生命由妥拉塑造——他们要把它系在额上（出13:9, 16; 28:38；申6:8; 11:18）。他们转离神，不顺服他时，圣经就说他们有铜额（赛48:4，ESV）、娼妓的额（耶3:3，NASB）或坚硬的额（结3:7，ESV）。乌西雅王犯罪篡夺祭司职任，就被击打，额上长出大麻风（代下26:16－21）。</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="box-sizing: border-box;"><span style="color: #FF8124; box-sizing: border-box;">神的子民是额上有神印记的人</span><span style="color: #000000; box-sizing: border-box;">。这一喻象内涵深刻，因为约翰的异象随后揭示出，神的忠心子民额上有神的名和羔羊的名（14:1; 22:4）。既然人的名字体现了其本质，额上有神的名就指此人的本质与神的本质渐趋一致。</span><span style="letter-spacing: 0.4px; box-sizing: border-box;">异象表明，作神的子民不仅意味着属于一个特殊的群体，加入一个特别的组织或名列某张会员名单。作神的子民意味着此人的本质得以恢复为神的形像。用保罗的话说，就是效法基督的模样（见罗8:29；林前15:49；林后3:17－18；弗4:13, 15, 24；腓3:21；西3:10）。</span></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; box-sizing: border-box;">谁有资格经历这样的转变？</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="letter-spacing: 0.4px; box-sizing: border-box;">约翰时代的犹太人通常会认为，神子民只限于具有犹太血统的人，或至少是在犹太传统中成长的人。他们认为外人不可能成为神的子民。约翰的异象揭示出，所有人都有资格成为神国度的一员。忠信的以色列人当然身在其中，但一切得救的人也都加入了他们的行列。7:9一开始，我们就看见许多的人“从各国、各族、各民、各方来”，他们是外邦人，现在站在神和羔羊的宝座前。外邦基督徒现在被纳入了神的国度。这些外邦信徒在得救者的行列中，因为他们身穿白衣，手拿棕树枝（见7:9注解），并且“用羔羊的血把衣裳洗白净了”（见7:14注解）。他们如今成为得赎的神子民，也分担了祭司的职任，侍奉神和羔羊。</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;">约翰的异象只提出两个作神子民的标准。</span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="box-sizing: border-box;"><span style="color: #FF8124; box-sizing: border-box;">第一，我们要被羔羊的血洗净。</span><span style="color: #000000; box-sizing: border-box;">异象用这种方式描述保罗所说的与基督同钉十字架（加2:20），或耶稣所说的为他失丧生命（太10:39）。这是向虚假的自我死去，就是被世界的价值标准和思想观念塑造的自我；这是脱去整套自我中心的生存方式，这种生存方式原本支配着我们每个思想、言语和行为；这是钉死整个自我保护的人际关系结构，这种结构旨在操控他人、达成我们自己的计划。同样，用保罗的话说，就是脱去旧人和旧人的行为（西3:9）。换言之，“用羔羊的血洗净”不仅是处理我们生命表面的污秽和斑点，如果只是那样，就触及不到我们本性深处。我们要在本性的最深处向神敞开自己，离弃我们旧的存在架构，将我们过去和现在的本相完全放手交给神。</span></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="color: #000000; box-sizing: border-box;"><br style="box-sizing: border-box;"/></span>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<span style="box-sizing: border-box;"><span style="color: #FF8124; box-sizing: border-box;">其次，要作神的子民，我们必须按着基督的本性被塑造改变。</span><span style="color: #000000; box-sizing: border-box;">我们要让神的名刻在我们的额上。这意味着我们要穿上新的本性，就是按着神的形像不断被更新（西3:10）。这是一种彻底的门徒生命，我们任凭神按着基督的形像日日塑造我们，达到完全。这样作门徒是一个动态、持续的过程，我们借此“凡事长进，连于元首基督”（弗4:15，NRSV），直到“我们众人在真道上同归于一，认识神的儿子，得以长大成人，满有基督长成的身量”（弗4:13，NRSV）。这种彻底的门徒生命是活“在宝座前”的生命。神和羔羊成为塑造我们本性的中心，是我们在世生活的首要标准。我们“求在上面的事，那里有基督”（西3:1，RSV）。我们活着是为了在我们生活一切境遇中服侍神和羔羊，在我们所有人际关系和生活处境中作“神的祭司”。</span></span>
		</p>
