	<section style="padding: 0px 12px; letter-spacing: 0.4px; line-height: 1.9; font-size: 16px; color: rgb(0, 0, 0); box-sizing: border-box;" class="_135editor">
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			没有奥斯邦博士，就没有今天<span style="color: #000000; box-sizing: border-box;">《房角石圣经注释丛书》</span>的出版。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			格兰特·奥斯邦（Grant R. Osborne）是福音派神学界家喻户晓的名字，他著述的《基督教释经学手册： 释经学螺旋的原理与应用》自1991年问世以来，已经成为释经学经典读本。我们这些有幸亲身认识他、蒙他鼓励指教的人，按照中国人尊敬老师的习俗称他为“先生”，并不为过。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			在这位著作等身的大先生看来，“人生最大的殊荣和乐事，没有比研读神的话语更大的了”<span style="color: #FFCA00; box-sizing: border-box;">[1]</span>。他说自己毕生研读圣经，传授心得，为的是让话语侍奉者懂得“<span style="color: #FF8124; box-sizing: border-box;">调制神话语的佳肴</span>”<span style="color: #FFCA00; box-sizing: border-box;">[2]</span>，让所有跟随主的人能够品出真道的美妙滋味。《房角石圣经注释丛书》里就留下了他倾注许多心血调制而成的大餐，而中文版的策划和推出更是处处刻着他的印记。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			故事要从新普及译本的翻译开始说起，大约在20多年前，先生和一班志同道合的圣经学者投入圣经英文译本 New Living Translation（NLT，新普及译本）的翻译，本意就是要借着今日的鲜活语言将古老经文的鲜活力量呈现给今日的读者。在这项译经的大工程里，先生是六位编委会成员之一，负责“福音书与使徒行传”组。该译本出版后，这个编委会定期开会讨论修订译文，以期跟上21世纪日新月异的语言发展。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			不久，先生又应丁道尔出版社之邀，以该译本为基础撰写注释书，推动读者跟文本更深入地互动。翻开《房角石圣经注释丛书》，先生的大名就列在首页的“英文版编辑团队”里，是“<span style="color: #FF8124; box-sizing: border-box;">新约顾问编辑</span>”。不仅如此，先生一人就承担了四卷书（<span style="color: #000000; box-sizing: border-box;">约翰福音、雅各书、彼得前后书</span>）的释经任务。在先生笔下，每一卷的注释虽然都洋洋洒洒，有四五百页之多，但无不简明清晰而又丰富深刻，是好读又耐读的释经作品，堪为整套房角石丛书的代表和典范。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			写释经书对先生而言，并不是智力的游戏、学术界的敲门砖，曾经牧养过教会、从未放下牧者心肠的他，把释经看作一个信仰传承的重任：若不按正意分解神的道，教会靠什么来推动宣教？热热闹闹的节目和活动，带来的影响看似广大，实则根基浅薄，不能长久，这是先生为之扼腕的。先生直言，“当前教会很需要<span style="color: #FF8124; box-sizing: border-box;">高品质的教导和讲道</span>，而这就要求教会配备圣经知识方面的优秀资源”。<span style="color: #FFCA00; box-sizing: border-box;">[3]</span>因此，当他飞越大洋来教授圣经神学时，得知因为种种历史原因，此地的教牧同工面对研经和讲道的任务，资料奇缺，无从入手，便想到刚完成不久的<span style="color: #000000; box-sizing: border-box;">《房角石圣经注释丛书》</span>，因这套书学术根基扎实、论述深入浅出，解明文本的同时也联系实际的应用，仿佛正是神为此时此地的需要量身定做的。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			事就这样开始，事就这样成了，其中许多曲折，难以尽数，但先生持之以恒地代祷，尽其所能地支持，直到他生命的末了。2018年夏天，虚弱的他无法再次长途旅行来到我们当中，在视频连线通话时，大家问到他的近况，他一如既往地用圣经回答我们，说<span style="color: #FF8124; box-sizing: border-box;">提摩太后书2:15</span>就是他的志业：“<span style="color: #000000; box-sizing: border-box;">你当竭力在神面前得蒙喜悦，作无愧的工人，按着正意分解真理的道。</span>”只要还能呼吸，还能打字，他就没有理由停下。他也用这话勉励我们，因为在他看来，所有蒙召在神话语上服侍教会之人，没有理由不持定这追求。
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			<br style="box-sizing: border-box;"/>
		</p>
		<p style=" margin: 0px; padding: 0px; box-sizing: border-box;">
			2018年冬天，先生归回天家。疾病缠身、工作辛劳的他因着神话语的滋养和扶持，喜乐满溢、热忱满怀地奔跑了一生，问心无愧地到喜悦他的神面前了。我们能透过他为房角石所撰写的书卷，再次聆听他为我们讲解圣经，这固然是幸事。但我们若能从阅读房角石起步，效法先生以研经为乐事，以话语服侍为荣幸，那才是先生所期待、神所喜悦的。
		</p>
